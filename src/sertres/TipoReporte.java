package sertres;

public class TipoReporte {

	int id_tipo_reporte;
	String tipo_reporte;
	String descripcion;
	
	public int getId_tipo_reporte() {
		return id_tipo_reporte;
	}
	public void setId_tipo_reporte(int id_tipo_reporte) {
		this.id_tipo_reporte = id_tipo_reporte;
	}
	public String getTipo_reporte() {
		return tipo_reporte;
	}
	public void setTipo_reporte(String tipo_reporte) {
		this.tipo_reporte = tipo_reporte;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
	
}
